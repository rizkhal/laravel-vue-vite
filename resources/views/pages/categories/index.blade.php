        <script type="text/javascript">
            $(document).ready(function() {
                const modal = $('#modal');
                const error = $('#category-error');
                const input = $('input[name="category"]');
                const datatable = $('#categorydatatable').DataTable();

                $(document).on('click', '.btn-close-modal', () => modal.modal('toggle'));

                const removeError = () => {
                    error.text('');
                    input.removeClass('is-invalid');
                }

                $('.btn-create').click(function() {
                    input.focus();
                    $('.btn-submit').text('Tambah');
                    $('#form').attr('role', 'create');

                    removeError();
                    input.val('');
                    modal.modal('show');
                });

                $(document).on('click', '.btn-edit', function() {
                    input.focus();
                    $('.btn-submit').text('Ubah');
                    $('#form').attr('role', 'edit');

                    let id = $(this).data('id');
                    let name = $(this).data('name');

                    removeError();
                    input.val(name);
                    $('#form').attr('update-id', id);
                    modal.modal('show');
                });

                $(document).on('submit', '#form', function(e) {
                    e.preventDefault();

                    let url, data, _ = $(this);

                    const isCreate = _.attr('role') === 'create';

                    if (isCreate) {
                        url = "{{ route('v1.categories.store') }}";
                        data = {
                            name: input.val()
                        }
                    } else {
                        url = "{{ url('api/v1/categories') . '/' }}" + _.attr('update-id');
                        data = {
                            _method: "PUT",
                            name: input.val(),
                        };
                    }

                    $.ajax({
                        url: url,
                        method: 'POST',
                        data: data,
                        success: function(data) {
                            modal.modal('hide');

                            if (isCreate) {
                                Swal.fire('Tersimpan!', 'Kategori passion berhasil disimpan',
                                    'success');
                            } else {
                                Swal.fire('Diubah!', 'Kategori passion berhasil diubah',
                                    'success');
                            }

                            datatable.ajax.reload();
                        },
                        error: function(e) {
                            let ee = JSON.parse(e.responseText);

                            if (ee) {
                                error.text(ee.message);
                                $('input[name="category"]').addClass('is-invalid');
                            }
                        }
                    });
                });

                $(document).on('click', '.btn-destroy', function() {
                    let _ = $(this);

                    Swal.fire({
                        title: 'Apakah anda yakin?',
                        text: "Tindakan ini akan menghapus kategori secara permanan",
                        icon: 'warning',
                        showCancelButton: true,
                        confirmButtonColor: '#3085d6',
                        cancelButtonColor: '#d33',
                        confirmButtonText: 'Ya, saya yakin!',
                        cancelButtonText: 'Batal'
                    }).then((result) => {
                        if (result.isConfirmed) {
                            $.ajax({
                                url: '{{ url('api/v1/categories') . '/' }}' + _.data('id'),
                                type: 'POST',
                                data: {
                                    "_method": "DELETE"
                                },
                                success: ({
                                    message
                                }) => {
                                    Swal.fire('Dihapus!', message, 'success');
                                    datatable.ajax.reload();
                                },
                            })
                        }
                    });
                });
            });
        </script>
